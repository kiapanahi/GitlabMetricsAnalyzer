    private static DimProject CreateSimpleProject(int projectId, string pathWithNamespace, string name, string defaultBranch, string visibility)
    {
        var now = DateTimeOffset.Now;
        var createdAt = now.AddDays(-Random.Shared.Next(30, 365));
        var lastActivityAt = createdAt.AddDays(Random.Shared.Next(0, (int)(now - createdAt).TotalDays));

        var pathParts = pathWithNamespace.Split('/');
        var namespacePath = string.Join("/", pathParts[..^1]);
        var namespaceName = pathParts[^2]; // Take the parent folder as namespace name

        return CreateProject(
            projectId,
            pathWithNamespace,
            name,
            defaultBranch,
            visibility,
            createdAt,
            lastActivityAt,
            Random.Shared.Next(1, 200), // Random creator ID
            namespaceName,
            namespacePath,
            "group");
    }

    private static int GetNamespaceId(string namespacePath)
    {
        // Simple hash-based ID generation for consistent namespace IDs
        return Math.Abs(namespacePath.GetHashCode()) % 1000 + 100;
    }

