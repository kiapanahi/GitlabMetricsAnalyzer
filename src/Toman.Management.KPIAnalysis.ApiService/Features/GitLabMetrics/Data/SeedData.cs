    /// <summary>
    /// Seeds the database with all 214 projects from your GitLab instance
    /// </summary>
    public static async Task SeedProjectsAsync(GitLabMetricsDbContext context, ILogger logger)
    {
        if (await context.DimProjects.AnyAsync())
        {
            logger.LogInformation("Projects already exist in database, skipping seed");
            return;
        }

        var seedProjects = GetSeedProjects();

        context.DimProjects.AddRange(seedProjects);
        await context.SaveChangesAsync();

        logger.LogInformation("Seeded {ProjectCount} projects into the database", seedProjects.Length);
    }

